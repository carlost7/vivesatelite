<?php

function getName($numerical_month)
{
      $spanish_months = array(
            'January' => 'Enero',
            'February' => 'Febrero',
            'March' => 'Marzo',
            'April' => 'Abril',
            'May' => 'Mayo',
            'June' => 'Junio',
            'July' => 'Julio',
            'August' => 'Agosto',
            'September' => 'Septiembre',
            'October' => 'Octubre',
            'November' => 'Noviembre',
            'December' => 'Diciembre'
      );

      $dateObj = DateTime::createFromFormat('!m', $numerical_month);
      return $spanish_months[$dateObj->format('F')];
}